/**
 * The status of a single scheduled medication dose. Stored on `Medication.doses`
 * keyed by `${yyyy-MM-dd}T${HH:mm}` — each calendar occurrence is addressable.
 */
export type DoseStatus = 'taken' | 'skipped' | 'missed';

export interface Medication {
  id: ID;
  name: string;
  /** Free-form dosage label, e.g. "500mg", "2 pills", "10 units". */
  dosage?: string;
  /** Optional short instruction shown on the calendar tile. */
  instructions?: string;
  /** Hex color used for the calendar event + module accents. */
  color: string;
  /** Optional emoji shown on the dose tile. Defaults to "💊" downstream. */
  emoji?: string;
  /** Times of day to take the medication (HH:mm, 24h). */
  times: string[];
  /**
   * Days of the week the schedule applies to (0 = Sunday … 6 = Saturday).
   * Empty array or undefined = every day.
   */
  daysOfWeek?: number[];
  /** ISO date (yyyy-MM-dd) — first day the schedule is active. */
  startDate: string;
  /** ISO date (yyyy-MM-dd) — last day, exclusive of indefinite courses. */
  endDate?: string;
  /** Minutes before the dose time to fire a reminder. */
  reminderMinutesBefore?: number;
  /** Free-form notes (prescribing doctor, side-effects to watch, etc.). */
  notes?: string;
  /**
   * Dose log keyed by occurrence (`yyyy-MM-ddTHH:mm`). Absent key = pending /
   * not yet acted on.
   */
  doses: Record<string, DoseStatus>;
  /** Optional contact who should be notified if a dose is missed. */
  caregiverEmail?: string;
  active: boolean;
  createdAt: string;
  updatedAt: string;
}

