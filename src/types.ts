export interface RecurrenceRule {
  freq: 'DAILY' | 'WEEKLY' | 'MONTHLY' | 'YEARLY';
  interval?: number;
  byweekday?: number[];
  count?: number;
  until?: string;
}

export interface CalendarEvent {
  id: ID;
  title: string;
  description?: string;
  start: string;
  end: string;
  allDay?: boolean;
  categoryId?: ID;
  tags?: string[];
  recurrence?: RecurrenceRule;
  recurrenceExceptions?: string[];
  reminders?: Reminder[];
  location?: string;
  /**
   * If set, this event was generated by another module (a task with a
   * scheduledStart, a trip item, etc.). UI surfaces use this to:
   *   - mark virtual events as read-only on the calendar grid
   *   - offer a "Jump to source" affordance in the event modal
   *   - avoid duplicating data on save (the source is authoritative)
   *
   * For wellness items (`medication`, `meal`, `workout`) the id is composed
   * as `<entityId>::<occurrenceKey>` so each instance is uniquely addressable
   * (a 2x/day medication has two events per day with distinct occurrence keys).
   */
  sourceRef?: { type: 'task' | 'tripItem' | 'trip' | 'goal' | 'medication' | 'meal' | 'workout'; id: ID };
  createdAt: string;
  updatedAt: string;
}

