/**
 * Lifestyle profile gathered during onboarding (and editable any time from
 * Personalize). Modules use these defaults to pre-fill new entries: Fitness
 * uses `preferredWorkoutTypes` + `workoutDaysPerWeek` when seeding a plan,
 * Meals uses `calorieTargetKcal` / `dietStyle` when logging meals, Sleep uses
 * `bedtime`/`wakeTime` for nightly reminders. Everything is optional so users
 * can skip the wellness step on first run.
 */
export interface WellnessProfile {
  // Fitness
  fitnessGoal?: FitnessGoal;
  fitnessLevel?: FitnessLevel;
  workoutDaysPerWeek?: number; // 0..7
  preferredWorkoutTypes?: WorkoutType[];
  equipment?: string[]; // free-form: 'gym', 'dumbbells', 'bands', 'kettlebell', 'bodyweight', 'cardio machine'
  heightCm?: number;
  weightKg?: number;

  // Diet
  dietStyle?: DietStyle;
  allergies?: string[]; // free-form: 'peanuts', 'gluten', 'dairy', ...
  dislikes?: string[];
  calorieTargetKcal?: number;
  proteinTargetG?: number;
  waterTargetMl?: number;

  // Sleep
  bedtime?: string; // HH:mm
  wakeTime?: string; // HH:mm
  sleepGoalHours?: number;

  // Mindfulness
  meditationMinutesPerDay?: number;
  journalingCadence?: JournalingCadence;
  gratitudePractice?: boolean;
}

